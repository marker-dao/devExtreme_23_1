import Popup from '../../../../../../ui/popup';
import { ProgressBarModel } from '../../../../../ui/__tests__/__mock__/model/progress_bar';
import { TextAreaModel } from '../../../../../ui/__tests__/__mock__/model/text_area';
import { ButtonModel } from '../../../../../ui/button/__tests__/__mock__/model/button';
const CLASSES = {
  wrapper: 'dx-overlay-wrapper',
  toolbarItem: 'dx-toolbar-item',
  stateInvisible: 'dx-state-invisible',
  stateDisabled: 'dx-state-disabled',
  aiPromptEditor: 'dx-ai-prompt-editor',
  aiPromptEditorTextArea: 'dx-ai-prompt-editor__text-area',
  aiPromptEditorRefreshButton: 'dx-ai-prompt-editor__refresh-button',
  aiPromptEditorApplyButton: 'dx-ai-prompt-editor__apply-button',
  aiPromptEditorStopButton: 'dx-ai-prompt-editor__stop-button',
  aiPromptEditorProgressBar: 'dx-ai-prompt-editor__progressbar'
};
export class AIPromptEditorModel {
  constructor(root) {
    this.root = root;
  }
  isVisible() {
    return this.getWrapperElement() !== null;
  }
  getPopupInstance() {
    return Popup.getInstance(this.root);
  }
  getTextArea() {
    return new TextAreaModel(this.getWrapperElement().querySelector(`.${CLASSES.aiPromptEditorTextArea}`));
  }
  getWrapperElement() {
    return document.body.querySelector(`.${CLASSES.aiPromptEditor}.${CLASSES.wrapper}`);
  }
  getRefreshButton() {
    return new ButtonModel(this.getWrapperElement().querySelector(`.${CLASSES.aiPromptEditorRefreshButton}`));
  }
  getApplyButton() {
    return new ButtonModel(this.getWrapperElement().querySelector(`.${CLASSES.aiPromptEditorApplyButton}`));
  }
  getStopButton() {
    return new ButtonModel(this.getWrapperElement().querySelector(`.${CLASSES.aiPromptEditorStopButton}`));
  }
  getProgressBar() {
    return new ProgressBarModel(this.getWrapperElement().querySelector(`.${CLASSES.aiPromptEditorProgressBar}`));
  }
  isApplyToolbarItemVisible() {
    const applyButton = this.getApplyButton().getElement();
    const toolbarItem = applyButton.closest(`.${CLASSES.toolbarItem}`);
    return !!toolbarItem && !toolbarItem.classList.contains(CLASSES.stateInvisible);
  }
  isStopToolbarItemVisible() {
    const stopButton = this.getStopButton().getElement();
    const toolbarItem = stopButton.closest(`.${CLASSES.toolbarItem}`);
    return !!toolbarItem && !toolbarItem.classList.contains(CLASSES.stateInvisible);
  }
}
