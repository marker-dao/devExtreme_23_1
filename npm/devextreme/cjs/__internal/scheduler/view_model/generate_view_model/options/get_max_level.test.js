"use strict";

var _globals = require("@jest/globals");
var _get_max_level = require("./get_max_level");
(0, _globals.describe)('getMaxLevel', () => {
  [false, true].forEach(isAdaptivityEnabled => {
    (0, _globals.it)(`should return -1 for maxAppointmentsPerCell=unlimited, adaptivityEnabled=${isAdaptivityEnabled}`, () => {
      (0, _globals.expect)((0, _get_max_level.getMaxLevel)({
        maxAppointmentsPerCell: 'unlimited',
        isAdaptivityEnabled
      })).toBe(-1);
    });
    (0, _globals.it)(`should return 3 for maxAppointmentsPerCell=3, adaptivityEnabled=${isAdaptivityEnabled}`, () => {
      (0, _globals.expect)((0, _get_max_level.getMaxLevel)({
        maxAppointmentsPerCell: 3,
        isAdaptivityEnabled
      })).toBe(3);
    });
  });
  (0, _globals.it)('should return calculated value for maxAppointmentsPerCell=auto, adaptivityEnabled=false, viewOrientation=horizontal', () => {
    (0, _globals.expect)((0, _get_max_level.getMaxLevel)({
      maxAppointmentsPerCell: 'auto',
      cellSize: {
        width: 150,
        height: 80
      },
      collectorSize: {
        width: 25,
        height: 20
      },
      viewOrientation: 'horizontal',
      isTimelineView: false,
      isAdaptivityEnabled: false
    })).toBe(3);
  });
  (0, _globals.it)('should return calculated value for maxAppointmentsPerCell=auto, adaptivityEnabled=false, viewOrientation=vertical', () => {
    (0, _globals.expect)((0, _get_max_level.getMaxLevel)({
      maxAppointmentsPerCell: 'auto',
      cellSize: {
        width: 150,
        height: 80
      },
      collectorSize: {
        width: 25,
        height: 20
      },
      viewOrientation: 'vertical',
      isTimelineView: false,
      isAdaptivityEnabled: false
    })).toBe(2);
  });
  (0, _globals.it)('should return calculated value for maxAppointmentsPerCell=auto, adaptivityEnabled=true, viewOrientation=horizontal', () => {
    (0, _globals.expect)((0, _get_max_level.getMaxLevel)({
      maxAppointmentsPerCell: 'auto',
      cellSize: {
        width: 150,
        height: 80
      },
      collectorSize: {
        width: 25,
        height: 20
      },
      viewOrientation: 'horizontal',
      isTimelineView: false,
      isAdaptivityEnabled: true
    })).toBe(0);
  });
  (0, _globals.it)('should return calculated value for maxAppointmentsPerCell=auto, adaptivityEnabled=true, viewOrientation=vertical', () => {
    (0, _globals.expect)((0, _get_max_level.getMaxLevel)({
      maxAppointmentsPerCell: 'auto',
      cellSize: {
        width: 150,
        height: 80
      },
      collectorSize: {
        width: 25,
        height: 20
      },
      viewOrientation: 'vertical',
      isTimelineView: false,
      isAdaptivityEnabled: true
    })).toBe(4);
  });
  (0, _globals.it)('should return 0 for cell size less then collector size, viewOrientation=vertical', () => {
    (0, _globals.expect)((0, _get_max_level.getMaxLevel)({
      maxAppointmentsPerCell: 'auto',
      cellSize: {
        width: 20,
        height: 20
      },
      collectorSize: {
        width: 30,
        height: 30
      },
      viewOrientation: 'vertical',
      isTimelineView: false,
      isAdaptivityEnabled: true
    })).toBe(0);
  });
  (0, _globals.it)('should return calculated value for timeline view, maxAppointmentsPerCell=auto, adaptivityEnabled=false, viewOrientation=horizontal', () => {
    (0, _globals.expect)((0, _get_max_level.getMaxLevel)({
      maxAppointmentsPerCell: 'auto',
      cellSize: {
        width: 150,
        height: 400
      },
      collectorSize: {
        width: 25,
        height: 20
      },
      viewOrientation: 'horizontal',
      isTimelineView: true,
      isAdaptivityEnabled: false
    })).toBe(6);
  });
});
