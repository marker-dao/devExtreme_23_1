"use strict";

var _globals = require("@jest/globals");
var _get_prev_interval_end_date = require("./get_prev_interval_end_date");
(0, _globals.describe)('getPrevIntervalEndDate', () => {
  (0, _globals.it)('should return correct prev end date for several intervals with gap', () => {
    (0, _globals.expect)((0, _get_prev_interval_end_date.getPrevIntervalEndDate)([{
      min: Date.UTC(2000, 0, 10, 3, 30),
      max: Date.UTC(2000, 0, 10, 23, 30)
    }, {
      min: Date.UTC(2000, 0, 11, 3, 30),
      max: Date.UTC(2000, 0, 11, 23, 30)
    }])).toEqual(Date.UTC(2000, 0, 9, 23, 30));
  });
  (0, _globals.it)('should return correct prev end date for several intervals without gap', () => {
    (0, _globals.expect)((0, _get_prev_interval_end_date.getPrevIntervalEndDate)([{
      min: Date.UTC(2000, 0, 10, 23, 30),
      max: Date.UTC(2000, 0, 11, 23, 30)
    }, {
      min: Date.UTC(2000, 0, 11, 23, 30),
      max: Date.UTC(2000, 0, 12, 23, 30)
    }])).toEqual(Date.UTC(2000, 0, 10, 23, 30));
  });
  (0, _globals.it)('should return correct prev end date for several intervals with gap at start of day', () => {
    (0, _globals.expect)((0, _get_prev_interval_end_date.getPrevIntervalEndDate)([{
      min: Date.UTC(2000, 0, 10, 9),
      max: Date.UTC(2000, 0, 12, 0)
    }, {
      min: Date.UTC(2000, 0, 12, 9),
      max: Date.UTC(2000, 0, 14, 0)
    }])).toEqual(Date.UTC(2000, 0, 10, 0));
  });
  (0, _globals.it)('should return correct prev end date for several intervals with gap at end of day', () => {
    (0, _globals.expect)((0, _get_prev_interval_end_date.getPrevIntervalEndDate)([{
      min: Date.UTC(2000, 0, 10, 0),
      max: Date.UTC(2000, 0, 11, 9)
    }, {
      min: Date.UTC(2000, 0, 12, 0),
      max: Date.UTC(2000, 0, 13, 9)
    }])).toEqual(Date.UTC(2000, 0, 9, 9));
  });
});
